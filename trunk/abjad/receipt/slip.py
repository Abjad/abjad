from abjad.core.abjadcore import _Abjad


class _SlipReceipt(_Abjad):
   '''Well structured references attaching to some Abjad component.'''

   def __init__(self, component, parentage, children, spanners):
      self._children = children
      self._component = component
      self._parentage = parentage
      self._spanners = spanners

   ## PRIVATE METHODS ##

   def _empty(self):
      self._children = None
      self._component = None
      self._parentage = None
      self._spanners = None
